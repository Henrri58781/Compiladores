    private void btnExpresionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExpresionActionPerformed
        String resultado = "";
        final Pattern pattern = Pattern.compile(areaExpresion.getText(), Pattern.MULTILINE);
        final Matcher matcher = pattern.matcher(contenidoArchivo.getText());

        while (matcher.find()) {
            resultado += matcher.group(0) + "\n";
            for (int i = 1; i <= matcher.groupCount(); i++) {
                resultado += matcher.group(i) + "\n";

            }
        }

        resultadoOperaciones.setText(resultado);
    }//GEN-LAST:event_btnExpresionActionPerformed

